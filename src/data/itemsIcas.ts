const norm = (z?: string) => (z ?? '').normalize('NFD').replace(/[̀-ͯ]/g,'').toUpperCase().trim();

export const getItemsIcasPorZona = (zona?: string): ItemLinea[] => {
  if (!zona) return ITEMS_ICAS;
  const z = norm(zona);
  return ITEMS_ICAS.filter(it => norm(it.zonaIca) === z);
};
